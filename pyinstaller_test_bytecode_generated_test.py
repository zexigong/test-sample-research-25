from PyInstaller.depend import bytecode


@pytest.fixture
def hello_world():
    return compile("print('Hello, world!')", "<string>", "exec")


def test_function_calls(hello_world):
    calls = bytecode.function_calls(hello_world)
    assert calls == [("print", ["Hello, world!"])]


def test_recursive_function_calls(hello_world):
    calls = bytecode.recursive_function_calls(hello_world)
    assert calls == {hello_world: [("print", ["Hello, world!"])]}


def test_extended_arguments():
    # When disassembled, this appears as
    # 0 LOAD_CONST               0 (('a', 'b', 'c'))
    # 2 RETURN_VALUE
    code = CodeType(
        0,
        0,
        0,
        67,
        b"t\x00S\x00",
        (("a", "b", "c"),),
        (),
        (),
        "",
        "",
        0,
        b"",
    )
    calls = bytecode.function_calls(code)
    assert calls == []


def test_search_recursively(hello_world):
    def dummy_search(code):
        return [code]

    search = bytecode.search_recursively(dummy_search, hello_world)
    assert search == {hello_world: [hello_world]}


def test_any_alias():
    assert list(bytecode.any_alias("foo.bar.whizz")) == ["foo.bar.whizz", "bar.whizz", "whizz"]


